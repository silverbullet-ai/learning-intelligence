# Creating sets
my_set = {1, 2, 3, 4}

print(my_set)

# Duplicate removal
nums = {1, 2, 2, 3, 4, 4, 5}

print(nums)

# Adding elements
my_set.add(5)
print(my_set)

# Removing elements
my_set.remove(2)
print(my_set)

# Membership test
print(3 in my_set)

# Iterating
for item in my_set:
    print(item)